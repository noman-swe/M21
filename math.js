// Math.ceil -takes the higher number
const myNumber2 = 14.4534;
const ceilOutput = Math.ceil(myNumber2);
console.log(ceilOutput);

// Math.floor -takes the lower number
const myNumber3 = 65.35;
const floorOutput = Math.floor(myNumber3);
// round
const roundOutput = Math.round(myNumber3);
// random
const random = Math.random() * 10;

const randomRound = Math.round(random);

console.log(floorOutput);
console.log(roundOutput);
console.log(random);
console.log(randomRound);
